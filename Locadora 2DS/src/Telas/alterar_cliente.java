    private void btn_S_ALcliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_S_ALcliActionPerformed

        Tabelas.Client client = new Tabelas.Client();
        //JOptionPane.showConfirmDialog(null, "aaaaaaaaaaaaaaaa");
        client.setCpf(Integer.valueOf(ALcpf_cli.getText()));
        client.setNome(ALnome_cli.getText());
        client.setEnde(ALend_cli.getText());
        client.setTel(ALtel_cli.getText());
        client.setDataNasc(Date.valueOf(ALnasc_cli.getText()));
        client.setCidade(ALcity_cli.getText());
        client.setSexo((String) ALsexo_cli.getSelectedItem());

        clientDAO cDAO = new clientDAO();
        cDAO.Salvar(client);
        bloqcampos();
        JOptionPane.showMessageDialog(null, "Alteração bem sucedida. \n clique no botão ATUALIZAR pra ver a alteração");
        dispose();
    }//GEN-LAST:event_btn_S_ALcliActionPerformed

