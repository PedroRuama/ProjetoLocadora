    public void CarregarCliente() {
        

        ALcpf_cli.setText(String.valueOf(ClienteTemp.getCpf()));
        ALnome_cli.setText(ClienteTemp.getNome());
        ALend_cli.setText(ClienteTemp.getEnde());
        ALtel_cli.setText(ClienteTemp.getTel());
        ALcity_cli.setText(ClienteTemp.getCidade());
        ALnasc_cli.setText(String.valueOf(ClienteTemp.getDataNasc()));
        
        if (ClienteTemp.getSexo().equals("M")) {
            ALsexo_cli.setSelectedIndex(1);
        } else {
            ALsexo_cli.setSelectedIndex(2);
        }

    }

