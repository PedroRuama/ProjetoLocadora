    private void table_cliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_cliMouseClicked
        btn_editCAD_cli.setEnabled(true);

        //CARREGAR DADOS NA CLASSE TEMPORARIA
        int indice = table_cli.getSelectedRow();
        client = (Tabelas.Client) ListaClientes.get(indice);

        ClienteTemp clientetemp = new ClienteTemp();
        clientetemp.setCpf(Integer.valueOf(client.getCpf()));
        clientetemp.setNome(client.getNome());
        clientetemp.setCidade(client.getCidade());
        clientetemp.setDataNasc(client.getDataNasc());
        clientetemp.setEnde(client.getEnde());
        clientetemp.setTel(client.getTel());
        clientetemp.setSexo(client.getSexo());
        
        
        
    }//GEN-LAST:event_table_cliMouseClicked

