    private void ALbtn_S_funcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ALbtn_S_funcActionPerformed
        Tabelas.Funcionario funcionario = new Tabelas.Funcionario();

        Departamento depto = new Departamento();
        depto = departamento.get(ALdepto_func.getSelectedIndex());

        funcionario.setMatricula(Integer.valueOf(ALmatricula_func.getText()));
        funcionario.setNome(ALnome_func.getText());
        funcionario.setDepto(depto.getCod());
        funcionario.setAdmissao(Date.valueOf(ALadm_func.getText()));
        funcionario.setFilho(Integer.valueOf(ALfilhos_func.getText()));
        funcionario.setSexo((String) ALsex_func.getSelectedItem());

        Double salario = Double.valueOf(ALsal_func.getText());
        funcionario.setSalario(BigDecimal.valueOf(salario));

        funcionarioDAO fDAO = new funcionarioDAO();
        fDAO.Salvar(funcionario);
        
        JOptionPane.showMessageDialog(null, "Alteração bem sucedida. \n clique no botão ATUALIZAR pra ver a alteração.");
        dispose();
    }//GEN-LAST:event_ALbtn_S_funcActionPerformed

