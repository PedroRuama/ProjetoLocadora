    public void CarregarFuncionario() {
        FuncTemp functemp = new FuncTemp();        
        ALmatricula_func.setText(String.valueOf(functemp.getMatricula()));
        ALnome_func.setText(functemp.getNome());
        ALdepto_func.setSelectedItem(String.valueOf(functemp.getDepto()));
        ALsal_func.setText(String.valueOf(functemp.getSalario()));
        ALadm_func.setText(String.valueOf(functemp.getAdmissao()));
        ALfilhos_func.setText(String.valueOf(functemp.getFilho()));

        if (functemp.getSexo().equals("M")) {
            ALsex_func.setSelectedIndex(1);
        } else {
            ALsex_func.setSelectedIndex(2);
        }

    }

    private void ListaDepartamento() {

        ALdepto_func.removeAllItems();
        departamentoDAO dDAO = new departamentoDAO();
        departamento = dDAO.ListaDepartamento();

        DefaultComboBoxModel combo = (DefaultComboBoxModel) ALdepto_func.getModel();
        for (int i = 0; i < departamento.size(); i++) {
            Departamento depto = departamento.get(i);
            combo.addElement(depto.getDepto());
            System.out.print(i);
        }

        ALdepto_func.repaint();

    }

