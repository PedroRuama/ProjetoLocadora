    public void ListaDepartamento() {
        depto_func.removeAllItems();
        departamentoDAO dDAO = new departamentoDAO();
        departamento = dDAO.ListaDepartamento();

        DefaultComboBoxModel combo = (DefaultComboBoxModel) depto_func.getModel();
        for (int i = 0; i < departamento.size(); i++) {
            Departamento depto = departamento.get(i);
            combo.addElement(depto.getDepto());
        }

        depto_func.repaint();

    }
