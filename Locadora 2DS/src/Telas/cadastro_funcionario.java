    private void btn_S_funcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_S_funcActionPerformed
        Tabelas.Funcionario funcionario = new Tabelas.Funcionario();

        Departamento depto = new Departamento();
        depto = departamento.get(depto_func.getSelectedIndex());

        funcionario.setMatricula(Integer.valueOf(matricula_func.getText()));
        funcionario.setNome(nome_func.getText());
        funcionario.setDepto(depto.getCod());
        funcionario.setAdmissao(Date.valueOf(adm_func.getText()));
        funcionario.setFilho(Integer.valueOf(filhos_func.getText()));
        funcionario.setSexo((String) sex_func.getSelectedItem());

        Double salario = Double.valueOf(sal_func.getText());
        funcionario.setSalario(BigDecimal.valueOf(salario));

        funcionarioDAO fDAO = new funcionarioDAO();
        fDAO.Salvar(funcionario);
        JOptionPane.showMessageDialog(null, "Alteração bem sucedida. \n clique no botão ATUALIZAR pra ver o novo cadastro.");
        dispose();
    }//GEN-LAST:event_btn_S_funcActionPerformed

